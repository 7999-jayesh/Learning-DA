// Bitwise Operator :-
#include<iostream>
using namespace std;
int main()
{
    int a=20,b=20,c;
    c=a&b; // Bitwise and{&} operator if both are same than it provide output otherwise it give 0.
    cout<<c<<endl;
    c=a|b;// Bitwise or{|} operator if both same then well and good other wise it also give same output.
    cout<<c<<endl;



    return 0;
}
